//Tipos de variables en JavaScript

// A la hora de declarar variables en JavaScript, debes tener en cuenta el lowerCamelCase, 
// que es la convención de nomenclatura que se utiliza en JavaScript para nombrar variables y funciones.

// Tipo var 

var helloWorld = "Hello, welcome to JavaScript!"
console.log(helloWorld)

helloWorld = "Hola de nuevo, bienvenido a JavaScript!"
console.log(helloWorld)


// Tipo let

let helloWorld2 = "Hello, welcome to JavaScript!"
console.log(helloWorld2)

helloWorld2 = "Hola de nuevo, bienvenido a JavaScript!"
console.log(helloWorld2)

// Tipo const

const helloWorldConst = "Hello, welcome to JavaScript!"
console.log(helloWorldConst)


